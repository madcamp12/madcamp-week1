import com.example.week1.screens.GalleryScreen
import com.example.week1.screens.Tap3Screen


@OptIn(ExperimentalMaterial3Api::class)
@Preview
@Composable
fun BottomNavigationBar() {
    val navController = rememberNavController()
    val navBackStackEntry by navController.currentBackStackEntryAsState()
    val currentDestination = navBackStackEntry?.destination
    Scaffold(
        modifier = Modifier.fillMaxSize(),
        bottomBar = {
            NavigationBar {
                BottomNavigationItem().bottomNavigationItems().forEachIndexed { _, navigationItem ->
                    NavigationBarItem(
                        selected = navigationItem.route == currentDestination?.route,
                        label = {
                            Text(navigationItem.label)
                        },
                        icon = {
                            Icon(
                                navigationItem.icon,
                                contentDescription = navigationItem.label
                            )
                        },
                        onClick = {
                            navController.navigate(navigationItem.route) {
                                popUpTo(navController.graph.findStartDestination().id) {
                                    saveState = true
                                }
                                launchSingleTop = true
                                restoreState = true
                            }
                        }
                    )
                }
            }
        }
    ) {paddingValues ->
        NavHost(
            navController = navController,
            startDestination = Screens.Contact.route,
            modifier = Modifier.padding(paddingValues = paddingValues)) {
            composable(Screens.Contact.route) {
                ContactScreen(
                    navController
                )
            }
            composable(Screens.Gallery.route) {
                GalleryScreen(
                    navController
                )
            }
            composable(Screens.Tap3.route) {
                Tap3Screen(
                    navController
                )
            }
        }
    }
}